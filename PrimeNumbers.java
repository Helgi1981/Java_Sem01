// - Цикл от 2 до 1000 включительно:
// Программа итерирует через все числа от 2 до 1000. Начинаем с 2, так как 1
// не является простым числом.
// - Проверка, является ли текущее число простым:
// Для каждого числа вызывается метод isPrime, который проверяет, является ли
// число простым.
// - Вывод простого числа:
// Если число простое, оно выводится на экран с помощью System.out.println().

// 2. Метод isPrime
// - Проверка чисел меньше или равных 1:
// Если число меньше или равно 1, оно не является простым.
// - Проверка делимости числа:
// Метод проверяет делимость числа на все числа от 2 до квадратного корня из
// проверяемого числа.
// Использование квадратного корня: Если число nn делится на число ii, то
// результат деления будет меньше или равен квадратному корню n. Если nn не
// делится ни на одно число до квадратного корня n, то оно простое.

