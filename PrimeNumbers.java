// Вывести все простые числа от 1 до 1000. Каждое число на новой строке.

public class PrimeNumbers {
    public static void main(String[] args) {
        // Цикл от 2 до 1000 включительно
        for (int number = 2; number <= 1000; number++) {
            // Проверяем, является ли текущее число простым
            if (isPrime(number)) {
                // Если число простое, выводим его на экран
                System.out.println(number);
            }
        }
    }

    // Метод для проверки, является ли число простым
    public static boolean isPrime(int number) {
        // Числа меньше или равные 1 не являются простыми
        if (number <= 1) {
            return false;
        }
        // Проверяем делимость числа на все числа от 2 до квадратного корня из number
        for (int i = 2; i <= Math.sqrt(number); i++) {
            // Если число делится нацело на i, то оно не является простым
            if (number % i == 0) {
                return false;
            }
        }
        // Если число не делится ни на одно из проверенных чисел, оно простое
        return true;
    }
}

